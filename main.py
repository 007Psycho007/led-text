    def _shift(self,seq, n):
        n = n % len(seq)
        return seq[n:] + seq[:n]

    def clear(self):
        for i in range(0,len(self.rows)):
            for j in range(0,self.columns):
                self.rows[i][j] = (0,0,0)
            self.rows[i].write()
        self.letter_index = 0

    def generate_text(self,string,buff,cchannel=None, color=None):
        if cchannel != None:
            color_list = cchannel
        elif color != None:
            color_list = []
            for i in range(len(string)):
                color_list.append(color)
        else:
            print("No Color defined, using default Color Red")
            color = (255,0,0)
        if len(color_list) != len(string):
            print("Not equal")
        
        print(color_list)
        text = []
        for i in range(0,self.row_count):
            text.append([])
        for idtext,letter in enumerate(string):
            for idi, i in enumerate(self.letters[letter]):
                for idj, j in enumerate(i):
                    text[idi].append((j*color_list[idtext][0],j*color_list[idtext][1],j*color_list[idtext][2]))
                text[idi].append(self.off)
        for i in range(0,self.row_count):
            for j in range(0,buff):
                text[i].append(self.off)
        self.text = text

    def write_text(self):
        if self.text != False: 
            self._output(self.text)
        else: 
            print("No Text defined")
            
    def _output(self,text):
        for idi, i in enumerate(text):
            for idj, j in enumerate(i):
                self.rows[idi][idj] = j
                if idj == self.columns-1:
                    break
            self.rows[idi].write()

    def rotate_text(self,interval=1,speed=1):
        text = self.text
        while True:
            self._output(text)
            sleep(speed)
            for i in range(0,self.row_count):
                text[i] = self._shift(text[i],interval)

    def shift_text(self,interval=1):
            for i in range(0,self.row_count):
                self.text[i] = self._shift(self.text[i],interval)


